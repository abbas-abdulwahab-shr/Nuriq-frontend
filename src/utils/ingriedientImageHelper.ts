type IngredientName = keyof typeof optimizedImages

export function getIngredientImage(name: IngredientName) {
  const path = optimizedImages[name]
  if (!path) {
    return 'https://placeimg.dev/286x148/222222?text=Image+Not+Found&textColor=fff'
  } else {
    return path
  }
}

export function optimizedIngredientImageUrl() {
  const imageUrls = []
  const files = import.meta.glob('/src/natural_ingredients/*', {
    eager: true,
    import: 'default',
  })

  for (const file of Object.values(files)) {
    imageUrls.push(`${file}?w=286&h=148&format=webp&quality=80`)
  }
  console.log(imageUrls)
  return files
}
